package ltsa.lts.distribution;

/**
 * 
 * @author gsibay
 *
 */
public class DistributionTransformationException extends Exception {

	/**
	 * @param arg0
	 */
	public DistributionTransformationException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public DistributionTransformationException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public DistributionTransformationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
