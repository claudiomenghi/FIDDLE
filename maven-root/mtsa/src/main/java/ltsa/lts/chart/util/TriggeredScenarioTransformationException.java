package ltsa.lts.chart.util;

/**
 * @author gsibay
 *
 */
public class TriggeredScenarioTransformationException extends Exception {

	/**
	 * @param arg0
	 */
	public TriggeredScenarioTransformationException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public TriggeredScenarioTransformationException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public TriggeredScenarioTransformationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
